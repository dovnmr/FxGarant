<?php

namespace CC\TestBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * User
 */
class User
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var string
     */
    private $login;

    /**
     * @var string
     */
    private $password;

    /**
     * @var string
     */
    private $email;

    /**
     * @var string
     */
    private $secondname;

    /**
     * @var string
     */
    private $firstname;

    /**
     * @var string
     */
    private $patronymic;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
